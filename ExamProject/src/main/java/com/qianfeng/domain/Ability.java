package com.qianfeng.domain;

public class Ability {
    private String examinee_num;
    private String examinee_name;
    private String lijie;
    private String biaoda;
    private String biancheng;

    public String getExaminee_num() {
        return examinee_num;
    }

    public void setExaminee_num(String examinee_num) {
        this.examinee_num = examinee_num;
    }

    public String getExaminee_name() {
        return examinee_name;
    }

    public void setExaminee_name(String examinee_name) {
        this.examinee_name = examinee_name;
    }

    public String getLijie() {
        return lijie;
    }

    public void setLijie(String lijie) {
        this.lijie = lijie;
    }

    public String getBiaoda() {
        return biaoda;
    }

    public void setBiaoda(String biaoda) {
        this.biaoda = biaoda;
    }

    public String getBiancheng() {
        return biancheng;
    }

    public void setBiancheng(String biancheng) {
        this.biancheng = biancheng;
    }
}
